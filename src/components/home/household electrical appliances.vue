      let that = this;
      let scrollTop =
        window.pageYOffset ||
        document.documentElement.scrollTop ||
        document.body.scrollTop;
      that.scrollTop = scrollTop;
      if (that.scrollTop > 1234) {
        let that = this;
        that.gotop = true;
      } else {
        that.gotop = false;
